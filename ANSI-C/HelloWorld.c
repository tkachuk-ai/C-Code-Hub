/* Prints a line of text */
#include <stdio.h>  // A preprocessor directive (stdio.h -> a standard input/output header)

// Function main begins execution
int main(){
    printf("Hello, World! Welcome to C!\n");
    printf("Hello, World\b");
    printf("\n");
    printf("Hello, \"World\"!");
    printf("\n");
    printf("Hello, World!\\");
    printf("\n");
    printf("Hello, World!\t");
} // end of function main


/* Escape characters to remember
   - \n new line
   - \t tab
   - \" quotes
   - \\ backslash itself
   - \b backspace
  */
